export class Contact {
    public id;
    public name;
    public email;
    public phone;
    public imageUrl;
    public group;

    constructor(id, name, email, phone, imageUrl, group){
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.imageUrl = imageUrl;
        this.group = group;

}
}